    private void botaoCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCalcularActionPerformed
        // TODO add your handling code here:
        float valor_inicial, taxa_juros, resultado;
        int num_meses;
        
        valor_inicial = Float.valueOf(this.caixaValorInicial.getText());
        taxa_juros = Float.valueOf(this.caixaTaxaJuros.getText());
        num_meses = Integer.valueOf(this.caixaMeses.getText());
        
        resultado = (float) (valor_inicial * Math.pow((1+(taxa_juros/100)), num_meses));
        
        this.caixaResultado.setText(String.format("%.2f", resultado));
    }//GEN-LAST:event_botaoCalcularActionPerformed

